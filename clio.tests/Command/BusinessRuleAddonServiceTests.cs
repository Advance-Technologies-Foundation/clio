	[Test]
	[Category("Unit")]
	[Description("Isolates blank and unknown names as per-item failures inside a mixed delete batch while the matched name still deletes with a single save.")]
	public void DeleteRules_Should_Isolate_Blank_And_Unknown_Names_When_Batch_Has_Mixed_Entries() {
		// Act
