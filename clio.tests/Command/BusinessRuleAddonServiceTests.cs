public sealed class BusinessRuleAddonServiceTests {

	private const string RuleAUId = "11111111-1111-1111-1111-111111111111";
	private const string ChildAUId = "22222222-2222-2222-2222-222222222222";
	private const string RuleBUId = "33333333-3333-3333-3333-333333333333";

	private IAddonSchemaDesignerClient _client = null!;
	private BusinessRuleAddonService _service = null!;
	private AddonSchemaDto? _savedSchema;

	[SetUp]
	public void SetUp() {
		_savedSchema = null;
		_client = Substitute.For<IAddonSchemaDesignerClient>();
		_client.GetSchema(Arg.Any<AddonGetRequestDto>()).Returns(_ => BuildSchema());
		_client
			.When(client => client.SaveSchema(Arg.Any<AddonSchemaDto>()))
			.Do(callInfo => _savedSchema = callInfo.Arg<AddonSchemaDto>());
		_service = new BusinessRuleAddonService(_client);
	}

	[Test]
	[Category("Unit")]
	[Description("Replaces the matched rule in place (order preserved), removes its old autogenerated children, appends the regenerated children, upserts the caption resource, and persists the whole batch with exactly one SaveSchema, ResetClientScriptCache, and BuildConfiguration.")]
	public void UpdateRules_Should_Replace_Rule_In_Place_And_Regenerate_Children_When_Name_Matches() {
		// Arrange
		BusinessRuleMetadataDto generatedParent = CreateGeneratedRule("Rule_A", caption: "New caption");
		string generatedParentUId = generatedParent.UId;
		BusinessRuleMetadataDto generatedChild = CreateGeneratedRule(
			$"Autogenerated_{generatedParentUId}_ClearValue",
			caption: $"ChildRule-{generatedParentUId}-ClearValue");
		generatedChild.ParentUId = generatedParentUId;

		// Act
		IReadOnlyList<BusinessRuleBatchItemResult> results = _service.UpdateRules(
			new AddonGetRequestDto(),
			[new BusinessRuleUpdateItem("Rule_A", false, [generatedParent, generatedChild])]);

		// Assert
		results.Should().ContainSingle(because: "one input item yields one outcome");
		results[0].Success.Should().BeTrue(because: "the rule name matched and the save succeeded");
		results[0].RuleName.Should().Be("Rule_A", because: "the outcome reports the updated rule name");
		_savedSchema.Should().NotBeNull(because: "a matched update must persist the mutated metadata");
		using JsonDocument metaData = JsonDocument.Parse(_savedSchema!.MetaData);
		JsonElement[] rules = [.. metaData.RootElement.GetProperty("rules").EnumerateArray()];
		rules.Should().HaveCount(3,
			because: "the replaced parent stays in place, the old child is removed, Rule_B survives, and the new child is appended");
		rules[0].GetProperty("name").GetString().Should().Be("Rule_A",
			because: "the replacement must keep the matched rule's position in the array");
		rules[0].GetProperty("uId").GetString().Should().Be(RuleAUId,
			because: "the grafter preserves the persisted rule uId on the replacement");
		rules[0].GetProperty("enabled").GetBoolean().Should().BeFalse(
			because: "the caller's explicit enabled=false intent must be applied");
		rules[1].GetProperty("name").GetString().Should().Be("Rule_B",
			because: "unrelated rules keep their position and content");
		rules[2].GetProperty("parentUId").GetString().Should().Be(RuleAUId,
			because: "the regenerated child is re-anchored to the preserved parent uId and appended");
		rules[2].GetProperty("name").GetString().Should().Be($"Autogenerated_{RuleAUId}_ClearValue",
			because: "the parent uId embedded in the regenerated child name is rewritten");
		rules.Should().NotContain(rule => rule.GetProperty("uId").GetString() == ChildAUId,
			because: "the old autogenerated child of the replaced rule must be removed");
		_savedSchema.Resources.Should().Contain(resource =>
				resource.Key == $"{RuleAUId}.Caption" && resource.Value[0].Value == "New caption",
			because: "the caption resource of the replaced rule must be upserted with the new caption");
		_savedSchema.Resources.Should().NotContain(resource => resource.Key == $"{ChildAUId}.Caption",
			because: "caption resources of removed children must be deleted");
		_client.Received(1).SaveSchema(Arg.Any<AddonSchemaDto>());
		_client.Received(1).ResetClientScriptCache();
		_client.Received(1).BuildConfiguration();
	}

	[Test]
	[Category("Unit")]
	[Description("Reports a per-item failure for an unknown rule name and performs no save when it is the only item of the batch.")]
	public void UpdateRules_Should_Report_Per_Item_Failure_Without_Saving_When_Name_Is_Unknown() {
		// Arrange
		BusinessRuleMetadataDto generatedParent = CreateGeneratedRule("Missing_Rule");

		// Act
